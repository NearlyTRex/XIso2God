            bw.Seek(8L, SeekOrigin.Begin);
            bw.Write(length - 0x400L);
            uint num2 = (uint) (length / 2048.0);
            bw.Seek(0x8050L, SeekOrigin.Begin);
            bw.Write(num2);
            bw.Endian = EndianType.BigEndian;
